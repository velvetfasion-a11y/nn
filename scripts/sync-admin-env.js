/**
 * Generates firestore.rules, storage.rules, and js/admin-config.public.js from .env.
 * Run before: firebase deploy --only firestore:rules,storage
 */
import "dotenv/config";
import { readFileSync, writeFileSync } from "fs";
import path from "path";
import { fileURLToPath } from "url";
import {
  ADMIN_EMAIL,
  ADMIN_UID,
  ADMIN_PAGE,
  ADMIN_PAGES,
} from "../server/admin-config.js";
import { FIREBASE_PUBLIC_CONFIG, PRODUCTION_HOSTS } from "../server/firebase-config.js";

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");

if (!ADMIN_UID || !ADMIN_EMAIL) {
  console.error("Missing ADMIN_UID or ADMIN_EMAIL in .env");
  process.exit(1);
}

const templates = [
  { template: "firestore.rules.template", output: "firestore.rules" },
  { template: "storage.rules.template", output: "storage.rules" },
];

for (const { template, output } of templates) {
  const templatePath = path.join(root, template);
  const outputPath = path.join(root, output);
  const content = readFileSync(templatePath, "utf8")
    .replace(/__ADMIN_UID__/g, ADMIN_UID)
    .replace(/__ADMIN_EMAIL__/g, ADMIN_EMAIL);

  writeFileSync(outputPath, content);
  console.log(`Wrote ${output} from ${template}`);
}

const publicConfig = `// Generated by npm run sync:admin — do not edit by hand.
window.__JJ_ADMIN__ = ${JSON.stringify(
  {
    email: ADMIN_EMAIL,
    uid: ADMIN_UID,
    page: ADMIN_PAGE,
    pages: ADMIN_PAGES,
  },
  null,
  2,
)};
`;

writeFileSync(path.join(root, "js/admin-config.public.js"), publicConfig);
console.log("Wrote js/admin-config.public.js");

const firebasePublicConfig = `// Generated by npm run sync:admin — do not edit by hand.
window.__JJ_FIREBASE__ = ${JSON.stringify(FIREBASE_PUBLIC_CONFIG, null, 2)};
window.__JJ_PRODUCTION_HOSTS__ = ${JSON.stringify(PRODUCTION_HOSTS, null, 2)};
`;

writeFileSync(path.join(root, "js/firebase-config.public.js"), firebasePublicConfig);
console.log("Wrote js/firebase-config.public.js");

console.log(`
Custom domain deploy checklist:
  • Firebase Console → Authentication → Settings → Authorized domains
    Add: ${PRODUCTION_HOSTS.join(", ")}
  • Push/deploy static files (CNAME → jamiljamila.com)
  • firebase deploy --only functions,firestore:rules,storage (email signups + admin uploads)
`);
